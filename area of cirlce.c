#include<stdio.h>
int main()
{
	// area of circle
	int r=4;
	float PI=3.14;
	printf("Area of cirlce :%f\n",PI*r*r);
	
	// area of square 
	int a=5;
	printf("area of square :%d\n",a*a);
	
	// area of rectangle 
	int l=12,b=5;
	printf("area of rectangle :%d\n",l*b);
	
	// area of triangle
	int c=6,d=7;
	printf("area of triangle :%d\n",c*d/2);
	
	return 0;
	
}
